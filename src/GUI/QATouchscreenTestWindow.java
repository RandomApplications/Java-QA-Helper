    private void zoneButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_zoneButtonMousePressed
        JButton touchedZoneButton = (JButton) evt.getSource();

        if (!touchedZoneButton.isContentAreaFilled() && ((evt.getModifiersEx() == InputEvent.getMaskForButton(java.awt.event.MouseEvent.BUTTON1)) || (System.getProperty("os.name").startsWith("Windows") && (evt.getModifiersEx() == InputEvent.getMaskForButton(java.awt.event.MouseEvent.BUTTON3))))) {
            // Also allow right-click on Windows since long touch on the screen in Windows it a right-click.

            touchedZoneButton.setForeground(Color.GRAY);
            touchedZoneButton.setContentAreaFilled(true);
            // These are the only effective button settings for decent styling on Linux. If I disable the button, it turns grey instead of green. If the content area is filled initially, then the buttons turn gray on mouse over before they have been touched. Currently, the buttons turn grey on mouse over (and then turn green on mouse exit) only on their first touch or after they've been touched before. This is the best I could do on Linux.

            JButton[] allZoneButtons = new JButton[]{zoneA1btn, zoneA2btn, zoneA3btn, zoneA4btn, zoneA5btn, zoneA6btn, zoneA7btn, zoneA8btn, zoneA9btn, zoneA10btn, zoneB1btn, zoneB2btn, zoneB3btn, zoneB4btn, zoneB5btn, zoneB6btn, zoneB7btn, zoneB8btn, zoneB9btn, zoneB10btn, zoneC8btn, zoneC9btn, zoneC4btn, zoneC2btn, zoneC5btn, zoneC7btn, zoneC1btn, zoneC10btn, zoneC3btn, zoneC6btn, zoneD3btn, zoneD8btn, zoneD9btn, zoneD6btn, zoneD10btn, zoneD1btn, zoneD2btn, zoneD7btn, zoneD4btn, zoneD5btn, zoneE8btn, zoneE1btn, zoneE3btn, zoneE9btn, zoneE5btn, zoneE7btn, zoneE10btn, zoneE2btn, zoneE6btn, zoneE4btn, zoneF1btn, zoneF5btn, zoneF10btn, zoneF2btn, zoneF6btn, zoneF8btn, zoneF7btn, zoneF9btn, zoneF4btn, zoneF3btn, zoneG3btn, zoneG7btn, zoneG9btn, zoneG4btn, zoneG6btn, zoneG5btn, zoneG1btn, zoneG2btn, zoneG8btn, zoneG10btn, zoneH1btn, zoneH2btn, zoneH3btn, zoneH4btn, zoneH5btn, zoneH6btn, zoneH7btn, zoneH8btn, zoneH9btn, zoneH10btn};

            boolean allZoneTapped = true;
            for (JButton thisZoneButton : allZoneButtons) {
                if (!thisZoneButton.isContentAreaFilled()) {
                    allZoneTapped = false;
                    break;
                }
            }

            if (allZoneTapped) {
                (new SwingWorker<Void, Void>() {
                    @Override
                    protected Void doInBackground() throws Exception {
                        TimeUnit.SECONDS.sleep(1);

                        return null;
                    }

                    @Override
                    protected void done() {
                        setTitle(getTitle() + "  —  PASSED");
                        getContentPane().setCursor(Cursor.getDefaultCursor());
                        dispose();
                    }
                }).execute();
            }
        }
    }//GEN-LAST:event_zoneButtonMousePressed

